package final07_31;

public class Define {
    //상수로 선언하는것들은 하나의 클래스를만들어서 정의 해놓는 것이좋다
    //이유는 어떤게 상수로 선언해놨는지 나중에 못찾기때문에 정리해놓는것이다
    //static 을 사용해서 가져다 사용하기 편하게 한다.
    public static final int MIN =1;         //최소값
    public static final int MAX = 99999;    //최대값

    public static final int ENG = 1001;     //영어과목
    public static final int MATH = 2001;    //수학과목

    public static final double PI = 3.14;   //원주율
    public static final String GOOD_MORNING = "Good Morning!";
}
